package com.itzcn.bean;

public class User {
	
	private String name;//用户的姓名
	private boolean role;//用户的角色
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean isRole() {
		return role;
	}
	public void setRole(boolean role) {
		this.role = role;
	}
}
